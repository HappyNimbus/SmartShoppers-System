package sample;

public class tableViewStore {
    private String store, ava, distance;



    public tableViewStore(String store, String ava, String distance) {
        this.store = store;
        this.ava = ava;
        this.distance = distance;
    }

    public String getStore() {
        return store;
    }

    public String getAva() {
        return ava;
    }

    public String getDistance() {
        return distance;
    }
}
