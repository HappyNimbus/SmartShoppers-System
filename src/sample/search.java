package sample;

public class search {
 private String name, cat, size, price, ava, isle, d;

 public search(String name, String cat, String size, String price, String ava, String isle, String d){
     this.name = name;
     this.cat = cat;
     this.size = size;
     this.price = price;
     this.ava = ava;
     this.isle = isle;
     this.d = d;

 }

    public String getName() {
        return name;
    }

    public String getCat() {
        return cat;
    }

    public String getSize() {
        return size;
    }

    public String getPrice() {
        return price;
    }

    public String getAva() {
        return ava;
    }

    public String getIsle() {
        return isle;
    }

    public String getD() {
        return d;
    }
}
